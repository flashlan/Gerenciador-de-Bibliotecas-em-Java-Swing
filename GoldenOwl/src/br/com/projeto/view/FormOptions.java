    private void btnSetLibraryLogoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSetLibraryLogoActionPerformed
        JFileChooser jfile = new JFileChooser();
        int result = jfile.showSaveDialog(this);
        File selectedFile = jfile.getSelectedFile();
        String filename = selectedFile.getName();
        if (filename.endsWith(".png") || filename.endsWith(".PNG")) {
            FileNameExtensionFilter filter = new FileNameExtensionFilter("*.image", "png");
            jfile.addChoosableFileFilter(filter);
            if (result == JFileChooser.APPROVE_OPTION) {
                File selectedImage = jfile.getSelectedFile();
                String path = selectedImage.getAbsolutePath();
                BufferedImage image;
                try {
                    image = ImageIO.read(selectedImage);
                    ImageIO.write(image, "png", new File("C:\\goldenOwl\\images\\" + "libraryLogo.png"));
                } catch (IOException ex) {
                    Logger.getLogger(Image.class.getName()).log(Level.SEVERE, null, ex);
                }
                try {
                    lblLibraryLogo.setIcon(ResizeLibraryLogoImage(path));
                    // imagePathStr = path;

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Image Error" + e.getMessage());
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Por Favor Seleciona um arquivo do tipo png", "Tente Novamente!", 1);
        }
    }//GEN-LAST:event_btnSetLibraryLogoActionPerformed

