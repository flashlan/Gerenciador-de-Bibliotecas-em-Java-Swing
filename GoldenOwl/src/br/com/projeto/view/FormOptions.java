private ImageIcon ResizeLibraryLogoImage(String imgPath) { //192x261
        int imageX = 143;
        int imageY = 146;
        lblLibraryLogo.setSize(imageX, imageY);

        ImageIcon myImage = new ImageIcon(imgPath);
        Image img = myImage.getImage();
        Image newImage = img.getScaledInstance(lblLibraryLogo.getWidth(), lblLibraryLogo.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon image = new ImageIcon(newImage);
        return image;
    }

